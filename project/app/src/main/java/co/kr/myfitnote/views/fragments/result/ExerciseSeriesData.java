package co.kr.myfitnote.views.fragments.result;

public class ExerciseSeriesData {
    String date;
    String value;
    String image;

    public String getDate() {
        return date;
    }

    public String getValue() {
        return value;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getImageUrl() {
        return image;
    }
}
